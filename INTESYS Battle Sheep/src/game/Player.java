package game;

public class Player {

	private String name;
	private int turnNo;
	
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getTurnNumber() {
		return turnNo;
	}
	public void setTurnNumber(int turnNo) {
		this.turnNo = turnNo;
	}
	
}
